package simuClasses;

public class StudentPair {
	private Student stud1;
	private Student stud2;
	private double  ourHappiness = 0;
	

	public Student getStud1() {
		return stud1;
	}

	public void setStud1(Student stud1) {
		this.stud1 = stud1;
	}

	public Student getStud2() {
		return stud2;
	}

	public void setStud2(Student stud2) {
		this.stud2 = stud2;
	}

	public double getOurHappiness() {
		return ourHappiness;
	}

	public void setOurHappiness(double ourHappiness) {
		this.ourHappiness = ourHappiness;
	}
}
